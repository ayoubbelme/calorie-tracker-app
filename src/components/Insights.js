/**
 * Returns 5 insights for the current day using a seeded daily shuffle.
 * The set changes every day automatically — no backend needed.
 */
export const getDailyInsights = () => {
    const now = new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const dayOfYear = Math.floor((now - start) / 86400000);

    const shuffled = [...ALL_INSIGHTS];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = (dayOfYear * 31 + i * 7) % (i + 1);
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled.slice(0, 5);
};